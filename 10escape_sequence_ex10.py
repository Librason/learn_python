tabby_cat = "\tI'am tabbed in."
# \t represents horizontal tab (TAB)
persian_cat = "I'm split\non a line."
# \n splits a string into two lines.
backslash_cat = "I'm \\ a \\ cat."
# \\ represents backslash (\)

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Caatnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)